# ── Core wizard ───────────────────────────────────────────────────────────────

def run_onboarding(
    config_path: str | Path | None = None,
    env_path: str | Path | None = None,
) -> dict[str, str]:
    """
    Run the interactive onboarding wizard.

    Parameters
    ----------
    config_path : path to config.json (defaults to ./config.json)
    env_path    : path to .env output  (defaults to ./.env)

    Returns
    -------
    dict[str, str]
        The configuration dict that was written to .env.
    """
    root = Path(os.path.dirname(os.path.abspath(__file__))).parent
    config_path = Path(config_path) if config_path else root / "config.json"
    env_path = Path(env_path) if env_path else root / ".env"

    config = _load_config(config_path)
    env = _load_env(env_path)

    agent_name = config.get("name", "Agent")
    agent_purpose = config.get("purpose", "Your AI assistant")
    integrations: list[str] = config.get("integrations", [])

    _print_banner(agent_name, agent_purpose)

    # ── Step 1: AI provider ────────────────────────────────────────────────
    print("  STEP 1 — Choose your AI provider\n")
    for i, (pid, desc, _key, _model) in enumerate(_AI_PROVIDER_PROMPTS, 1):
        print(f"  {i}. {desc}")

    while True:
        choice = _ask("\n  Enter number (1–5)", default="1")
        if choice.isdigit() and 1 <= int(choice) <= len(_AI_PROVIDER_PROMPTS):
            break
        print("  Please enter a number between 1 and 5.")

    provider_id, provider_label, key_env, default_model = _AI_PROVIDER_PROMPTS[int(choice) - 1]
    model = _ask(f"  Model name", default=default_model)

    env["AETHEERAI_DEFAULT_PROVIDER"] = provider_id
    env["AETHEERAI_DEFAULT_MODEL"] = model

    if key_env:
        existing = env.get(key_env, "")
        if existing:
            print(f"\n  {key_env} already set — press Enter to keep or type a new value.")
        key_val = _ask(key_env, default=existing, secret=True)
        if key_val:
            env[key_env] = key_val

    # ── Step 2: Integration credentials ───────────────────────────────────
    if integrations:
        print(f"\n  STEP 2 — Integration credentials\n")
        print(f"  This agent connects to: {', '.join(integrations)}\n")

        for integration in integrations:
            prompts = _INTEGRATION_PROMPTS.get(integration.lower(), [])
            if not prompts:
                # Generic fallback
                prompts = [
                    {"key": f"{integration.upper()}_API_KEY",
                     "label": f"{integration.title()} API key",
                     "secret": "yes"},
                ]

            print(f"  -- {integration.upper()} --")
            for p in prompts:
                key = p["key"]
                label = p["label"]
                is_secret = str(p.get("secret", "no")).lower() in ("yes", "true", "1")
                default_val = p.get("default", env.get(key, ""))
                value = _ask(label, default=default_val, secret=is_secret)
                if value:
                    env[key] = value
            print()

    # ── Step 3: Write .env ─────────────────────────────────────────────────
    _save_env(env_path, env)
    print(f"\n  Configuration saved to: {env_path}")

    # ── Step 4: Summary ────────────────────────────────────────────────────
    print(f"\n{_SEP}")
    print(f"  Setup complete! To start your agent:")
    print(f"    python run_agent.py")
    print(f"{_SEP}\n")

    return env


def configure(
    agent_name: str = "Agent",
    extra_prompts: list[dict[str, str]] | None = None,
    env_path: str | Path | None = None,
) -> dict[str, str]:
    """
    Minimal programmatic entry point matching the spec example.

    Extra prompts format:
        [{"key": "MY_KEY", "label": "My service API key", "secret": "yes"}]
    """
    root = Path(os.path.dirname(os.path.abspath(__file__))).parent
    env_path = Path(env_path) if env_path else root / ".env"
    env = _load_env(env_path)

    print(f"\n{_SEP}")
    print(f"  {agent_name} — Configuration")
    print(f"{_SEP}\n")

    for prompt in (extra_prompts or []):
        key = prompt.get("key", "")
        label = prompt.get("label", key)
        is_secret = str(prompt.get("secret", "no")).lower() in ("yes", "true", "1")
        default_val = prompt.get("default", env.get(key, ""))
        value = _ask(label, default=default_val, secret=is_secret)
        if value:
            env[key] = value

    _save_env(env_path, env)
    return env


# ── CLI entry ─────────────────────────────────────────────────────────────────

def main() -> None:
    parser = argparse.ArgumentParser(
        description="AetheerAI Agent — First-time onboarding wizard"
    )
    parser.add_argument(
        "--config", default=None,
        help="Path to config.json (default: ./config.json)"
    )
    parser.add_argument(
        "--env", default=None,
        help="Path to .env output file (default: ./.env)"
    )
    args = parser.parse_args()
    run_onboarding(config_path=args.config, env_path=args.env)


if __name__ == "__main__":
    main()
