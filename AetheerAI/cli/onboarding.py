"""onboarding.py — Interactive first-run configuration wizard for exported agents.

After a customer downloads a standalone agent package they run:

    python onboarding.py

The wizard:
  1. Reads config.json (AgentSpec) to know which integrations the agent needs.
  2. Prompts the customer only for the credentials that are actually required.
  3. Writes credentials to .env (never committed to source control).
  4. Verifies that required keys are present before finishing.

The wizard is designed to be embedded inside every exported agent package.
It can also be called programmatically from run_agent.py for first-time setup.

Usage (standalone)
------------------
    python onboarding.py
    python onboarding.py --config path/to/config.json --env path/to/.env

Usage (programmatic)
--------------------
    from cli.onboarding import run_onboarding
    run_onboarding()
"""

from __future__ import annotations

import argparse
import json
import os
import sys
from pathlib import Path
from typing import Any

# ── Helpers ──────────────────────────────────────────────────────────────────

_SEP = "=" * 60


def _print_banner(agent_name: str, agent_purpose: str) -> None:
    print(f"\n{_SEP}")
    print(f"  {agent_name}  —  First-time Setup")
    print(f"  {agent_purpose[:56]}")
    print(f"{_SEP}\n")


def _ask(prompt: str, default: str = "", secret: bool = False) -> str:
    """Prompt user for input, hiding text if secret=True."""
    suffix = f" [{default}]" if default and not secret else ""
    full_prompt = f"  {prompt}{suffix}: "
    try:
        if secret:
            import getpass
            value = getpass.getpass(full_prompt)
        else:
            value = input(full_prompt).strip()
    except (KeyboardInterrupt, EOFError):
        print("\n  Setup cancelled.")
        sys.exit(0)
    return value.strip() or default


def _load_config(config_path: Path) -> dict[str, Any]:
    if config_path.exists():
        try:
            return json.loads(config_path.read_text(encoding="utf-8"))
        except (json.JSONDecodeError, OSError):
            pass
    return {}


def _load_env(env_path: Path) -> dict[str, str]:
    """Parse existing .env file into a dict."""
    env: dict[str, str] = {}
    if not env_path.exists():
        return env
    for line in env_path.read_text(encoding="utf-8").splitlines():
        line = line.strip()
        if line and not line.startswith("#") and "=" in line:
            key, _, val = line.partition("=")
            env[key.strip()] = val.strip()
    return env


def _save_env(env_path: Path, data: dict[str, str]) -> None:
    """Write .env file preserving comment header."""
    lines = ["# Agent configuration — generated by onboarding.py\n"]
    for key, val in data.items():
        lines.append(f"{key}={val}\n")
    env_path.write_text("".join(lines), encoding="utf-8")


